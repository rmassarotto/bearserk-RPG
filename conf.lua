function love.conf(t)
    t.title = "Jogo LP"		-- The title of the window the game is in (string)
    t.author = "Eduardo e Rodrigo"		-- The author of the game (string)
    t.window.width = 1360			-- The window width (number)
    t.window.height = 768			-- The window height (number)
    t.window.fullscreen = false		-- Enable fullscreen (boolean)
    t.window.vsync = false			-- Enable vertical sync (boolean)
end
